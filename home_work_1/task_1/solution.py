number_day = int(input('Введите номер дня недели: '))

if number_day == 1:
    print('Monday')
elif number_day == 2:
    print('Tuesday')
elif number_day == 3:
    print('Wednesday')
elif number_day == 4:
    print('Thursday')
elif number_day == 5:
    print('Friday')
elif number_day == 6:
    print('Saturday')
elif number_day == 7:
    print('Sunday')
else:
    print('Error')
